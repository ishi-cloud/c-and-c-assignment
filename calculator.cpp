#include<stdio.h>
int i,j;
int main()
{printf("ENTER FIRST NUMBER\n");
scanf("%d",&i);
printf("ENTER SECOND NUMBER\n");
scanf("%d",&j);
printf("ADDITION OF FIRST NUMBER AND SECOND NUMBER IS : %d\n",(i+j));
printf("SUBTRACTION OF FIRST NUMBER AND SECOND NUMBER IS : %d\n",(i-j));
printf("MULTIPLICATION OF FIRST NUMBER AND SECOND NUMBER IS : %d\n",(i*j));
printf("DIVISION OF FIRST NUMBER AND SECOND NUMBER IS : %d\n",(i/j));
printf("FIRST NUMBER MODULUS SECOND NUMBER IS : %d\n",(i%j));
return 0;
}
